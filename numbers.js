{
    let number = prompt("Enter any of numbers 1, 10, 100, 1000 ---- ");
    if (number == 1)
        console.log("unit");
    else if (number == 10)
        console.log("ten");
    else if (number == 100)
        console.log("hundred");
    else if (number == 1000)
        console.log("thousand");
}